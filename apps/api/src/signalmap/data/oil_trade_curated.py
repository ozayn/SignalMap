def get_curated_edges(year: int) -> list[dict[str, Any]]:
    """Return curated edges for a year, or empty list if year not in dataset."""
    key = str(year)
    return list(OIL_TRADE_CURATED.get(key, []))


def get_curated_years(start_year: int, end_year: int) -> dict[str, list[dict[str, Any]]]:
    """Return curated edges for year range, filtered by start_year..end_year."""
    result: dict[str, list[dict[str, Any]]] = {}
    for y in range(start_year, end_year + 1):
        key = str(y)
        if key in OIL_TRADE_CURATED:
            result[key] = list(OIL_TRADE_CURATED[key])
    return result
